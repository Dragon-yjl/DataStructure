package jdkSort7_27;

public class Person {

    int age;
    String name;
    int height;
    int weight;

    @Override
    public String toString() {
        return "Person{" +
                "age=" + age +
                ", name='" + name + '\'' +
                ", height=" + height +
                ", weight=" + weight +
                '}';
    }
}
